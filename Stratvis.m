    );
%---------------------------------------------------------------------------------------------------------------------
%----------------------------------------------Tableau--------------------------------------------------------------
%---------------------------------------------------------------------------------------------------------------------
tab = uitable(...
    'Parent',f,...
    'Data',randi(100,10,3),...
    'Position',[50 20 1250 350]...
    );
tab.ColumnName = {'ROI','Temps de fixation','Temps total','Ordre de decouverte'};
tab.ColumnEditable = true;
