    'Parent', f, ...
    'Title', 'Chargement des données', ...
    'Units', 'Pixels', ...
    'FontSize', 10, ...
    'Position', [500, 400, 800, 320 ] ...
    );

handles.movie_scrn = axes( ...
    'Parent', v, ...
    'tag', 'movie_scrn', ...
    'Units', 'Pixels', ...
    'YTick', [], ...
    'XTick', [], ...
